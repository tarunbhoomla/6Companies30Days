/*
delete n nodes after m nodes
  The input list will have at least one element  
  Node is defined as 

struct Node
{
    int data;
    struct Node *next;
    
    Node(int x){
        data = x;
        next = NULL;
    }
    
};

*/
class Solution
{
    public:
    void linkdelete(struct Node  *head, int M, int N)
    {
      Node* curr = head ;
      while(curr)
       {
           for(int i=0 ; i<M-1 && curr != NULL ;i++)
             {
                curr = curr->next ;
             }
           if(curr==NULL) return  ;
           
          Node* delNode = curr->next ;
          for(int i=0 ; i<N && delNode != NULL ;i++)
          {
             Node* temp = delNode ;
             delNode = delNode->next ;
             delete(temp) ;
             
          }
         curr->next = delNode ;
         curr = delNode ;
       }
        
    }
};



